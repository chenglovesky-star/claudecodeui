/**
 * SKILL PACK API ROUTES
 * =====================
 *
 * Endpoints for collecting and packaging Claude CLI skills, commands,
 * and MCP server configs into downloadable installation scripts.
 */

import express from 'express';
import fs from 'fs/promises';
import path from 'path';
import os from 'os';
import { userMcpDb } from '../database/db.js';

const router = express.Router();

const CLAUDE_DIR = path.join(os.homedir(), '.claude');
const CLAUDE_JSON = path.join(os.homedir(), '.claude.json');
const COMMANDS_DIR = path.join(CLAUDE_DIR, 'commands');
const SKILLS_DIR = path.join(CLAUDE_DIR, 'skills');

// ─── Collectors ──────────────────────────────────────────────

/**
 * Recursively scan a directory for .md files.
 * @returns {Promise<Array<{relativePath: string, content: string}>>}
 */
async function collectCommandFiles(dir) {
  const results = [];
  try {
    const entries = await fs.readdir(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        const sub = await collectCommandFiles(fullPath);
        results.push(...sub);
      } else if (entry.isFile() && entry.name.endsWith('.md')) {
        const content = await fs.readFile(fullPath, 'utf-8');
        const relativePath = path.relative(COMMANDS_DIR, fullPath);
        results.push({ relativePath, content });
      }
    }
  } catch {
    // directory may not exist
  }
  return results;
}

/**
 * Scan ~/.claude/skills/ for SKILL.md files (one per skill folder).
 * @returns {Promise<Array<{name: string, content: string}>>}
 */
async function collectSkillFiles(dir) {
  const results = [];
  try {
    const entries = await fs.readdir(dir, { withFileTypes: true });
    for (const entry of entries) {
      if (entry.isDirectory()) {
        const skillFile = path.join(dir, entry.name, 'SKILL.md');
        try {
          const content = await fs.readFile(skillFile, 'utf-8');
          results.push({ name: entry.name, content });
        } catch {
          // no SKILL.md in this folder
        }
      }
    }
  } catch {
    // directory may not exist
  }
  return results;
}

function sanitizeMcpServers(servers) {
  const SENSITIVE_KEYS = /key|token|secret|password|credential|auth/i;
  const sanitized = {};

  for (const [name, cfg] of Object.entries(servers || {})) {
    const clone = JSON.parse(JSON.stringify(cfg));
    delete clone.headers;

    if (clone.env && typeof clone.env === 'object') {
      for (const envKey of Object.keys(clone.env)) {
        if (SENSITIVE_KEYS.test(envKey)) {
          clone.env[envKey] = '<YOUR_VALUE_HERE>';
        }
      }
    }

    sanitized[name] = clone;
  }

  return sanitized;
}

async function collectLegacyMcpServers() {
  try {
    const raw = await fs.readFile(CLAUDE_JSON, 'utf-8');
    const config = JSON.parse(raw);
    return config.mcpServers || {};
  } catch {
    return {};
  }
}

/**
 * Read MCP servers for the current user, with legacy ~/.claude.json fallback.
 * Settings UI stores MCP servers in DB, so the download pack needs the same source.
 * @returns {Promise<Record<string, object>>}
 */
async function collectMcpServers(userId) {
  const legacyServers = await collectLegacyMcpServers();

  if (!userId) {
    return sanitizeMcpServers(legacyServers);
  }

  let userServers = {};
  try {
    const userRows = userMcpDb.getAll(userId);
    userServers = userMcpDb.toSdkFormat(userRows);
  } catch (error) {
    console.warn('[skill-pack] Failed to read user MCP servers from DB, falling back to legacy config:', error?.message || error);
  }

  return sanitizeMcpServers({
    ...legacyServers,
    ...userServers,
  });
}

// ─── Script Generators ───────────────────────────────────────

/**
 * Sanitize a path component to prevent shell injection.
 * Only allow alphanumeric, hyphens, underscores, dots, and forward slashes.
 */
function safePath(p) {
  return p.replace(/[^a-zA-Z0-9_\-./]/g, '_');
}

/**
 * Generate a unique heredoc delimiter that doesn't collide with file content.
 */
function uniqueDelimiter(base, content) {
  let delim = base;
  let i = 0;
  while (content.includes(delim)) {
    i++;
    delim = `${base}_${i}`;
  }
  return delim;
}

/**
 * Generate a macOS .command installation script.
 */
function generateMacScript(commands, skills, mcpServers) {
  const lines = [];
  lines.push('#!/bin/bash');
  lines.push('# Claude CLI Skill Pack Installer (macOS)');
  lines.push('# Generated by Claude Code UI');
  lines.push('set -e');
  lines.push('');
  lines.push('CLAUDE_DIR="$HOME/.claude"');
  lines.push('COMMANDS_DIR="$CLAUDE_DIR/commands"');
  lines.push('SKILLS_DIR="$CLAUDE_DIR/skills"');
  lines.push('');
  lines.push('echo "========================================="');
  lines.push('echo "  Claude CLI Skill Pack Installer"');
  lines.push('echo "========================================="');
  lines.push('echo ""');
  lines.push('command_added=0');
  lines.push('command_skipped=0');
  lines.push('skill_added=0');
  lines.push('skill_skipped=0');
  lines.push('');

  // --- Commands ---
  for (const cmd of commands) {
    const relPath = safePath(cmd.relativePath);
    const dir = path.dirname(relPath);
    const delim = uniqueDelimiter('CMD_EOF', cmd.content);
    if (dir && dir !== '.') {
      lines.push(`mkdir -p "$COMMANDS_DIR/${dir}"`);
    } else {
      lines.push('mkdir -p "$COMMANDS_DIR"');
    }
    lines.push(`if [ -f "$COMMANDS_DIR/${relPath}" ]; then`);
    lines.push(`  echo "  ⊘ command (already exists): ${relPath}"`);
    lines.push('  command_skipped=$((command_skipped + 1))');
    lines.push('else');
    lines.push(`  cat > "$COMMANDS_DIR/${relPath}" << '${delim}'`);
    lines.push(cmd.content);
    lines.push(delim);
    lines.push(`  echo "  ✓ command: ${relPath}"`);
    lines.push('  command_added=$((command_added + 1))');
    lines.push('fi');
    lines.push('');
  }

  // --- Skills ---
  for (const skill of skills) {
    const skillName = safePath(skill.name);
    const delim = uniqueDelimiter('SKILL_EOF', skill.content);
    lines.push(`mkdir -p "$SKILLS_DIR/${skillName}"`);
    lines.push(`if [ -f "$SKILLS_DIR/${skillName}/SKILL.md" ]; then`);
    lines.push(`  echo "  ⊘ skill (already exists): ${skillName}"`);
    lines.push('  skill_skipped=$((skill_skipped + 1))');
    lines.push('else');
    lines.push(`  cat > "$SKILLS_DIR/${skillName}/SKILL.md" << '${delim}'`);
    lines.push(skill.content);
    lines.push(delim);
    lines.push(`  echo "  ✓ skill: ${skillName}"`);
    lines.push('  skill_added=$((skill_added + 1))');
    lines.push('fi');
    lines.push('');
  }

  // --- MCP Servers (merge via python3) ---
  if (Object.keys(mcpServers).length > 0) {
    const mcpJson = JSON.stringify(mcpServers);
    const delim = uniqueDelimiter('MCP_EOF', mcpJson);
    lines.push('# Merge MCP server configs into ~/.claude.json');
    lines.push('if ! command -v python3 &>/dev/null; then');
    lines.push('  echo "  ⚠ python3 not found, skipping MCP config merge"');
    lines.push('  echo "    You can manually add MCP servers to ~/.claude.json"');
    lines.push('else');
    lines.push(`MERGE_JSON=$(cat << '${delim}'`);
    lines.push(mcpJson);
    lines.push(`${delim}`);
    lines.push(')');
    lines.push('');
    lines.push(`python3 -c "
import json, os, sys

merge = json.loads(sys.argv[1])
config_path = os.path.expanduser('~/.claude.json')

if os.path.exists(config_path):
    with open(config_path, 'r') as f:
        config = json.load(f)
else:
    config = {}

servers = config.setdefault('mcpServers', {})
added = 0
skipped = 0
for name, cfg in merge.items():
    if name not in servers:
        servers[name] = cfg
        added += 1
        print(f'  ✓ mcp: {name}')
    else:
        skipped += 1
        print(f'  ⊘ mcp (already exists): {name}')

with open(config_path, 'w') as f:
    json.dump(config, f, indent=2, ensure_ascii=False)

print(f'  MCP: {added} added, {skipped} skipped')
" "$MERGE_JSON"`);
    lines.push('fi');
    lines.push('');
  }

  // --- Summary ---
  lines.push('echo ""');
  lines.push('echo "========================================="');
  lines.push('echo "  Installation Complete!"');
  lines.push('echo "========================================="');
  lines.push('echo "  Commands: ${command_added} added, ${command_skipped} skipped"');
  lines.push('echo "  Skills:   ${skill_added} added, ${skill_skipped} skipped"');
  lines.push(`echo "  MCP:      ${Object.keys(mcpServers).length}"`);
  lines.push('echo ""');
  lines.push('echo "Restart Claude CLI to apply changes."');
  lines.push('');

  return lines.join('\n');
}

/**
 * Generate a Windows .bat installation script.
 * Uses embedded Base64-encoded PowerShell for UTF-8 file writing and JSON merging.
 */
function generateWindowsScript(commands, skills, mcpServers) {
  // Build a PowerShell script that does everything
  const psLines = [];
  psLines.push('$ErrorActionPreference = "Stop"');
  psLines.push('$claudeDir = Join-Path $env:USERPROFILE ".claude"');
  psLines.push('$commandsDir = Join-Path $claudeDir "commands"');
  psLines.push('$skillsDir = Join-Path $claudeDir "skills"');
  psLines.push('');
  psLines.push('Write-Host "========================================="');
  psLines.push('Write-Host "  Claude CLI Skill Pack Installer"');
  psLines.push('Write-Host "========================================="');
  psLines.push('Write-Host ""');
  psLines.push('$commandAdded = 0; $commandSkipped = 0');
  psLines.push('$skillAdded = 0; $skillSkipped = 0');
  psLines.push('');

  // --- Commands ---
  for (const cmd of commands) {
    const dir = path.dirname(cmd.relativePath);
    const winPath = cmd.relativePath.replace(/\//g, '\\');
    const encoded = Buffer.from(cmd.content, 'utf-8').toString('base64');
    if (dir && dir !== '.') {
      const winDir = dir.replace(/\//g, '\\');
      psLines.push(`$d = Join-Path $commandsDir "${winDir}"`);
    } else {
      psLines.push('$d = $commandsDir');
    }
    psLines.push('if (-not (Test-Path $d)) { New-Item -ItemType Directory -Path $d -Force | Out-Null }');
    psLines.push(`$commandPath = Join-Path $commandsDir "${winPath}"`);
    psLines.push('if (Test-Path $commandPath) {');
    psLines.push(`  Write-Host "  SKIP command (already exists): ${cmd.relativePath}"`);
    psLines.push('  $commandSkipped++');
    psLines.push('} else {');
    psLines.push(`  $bytes = [Convert]::FromBase64String("${encoded}")`);
    psLines.push('  [System.IO.File]::WriteAllBytes($commandPath, $bytes)');
    psLines.push(`  Write-Host "  OK command: ${cmd.relativePath}"`);
    psLines.push('  $commandAdded++');
    psLines.push('}');
    psLines.push('');
  }

  // --- Skills ---
  for (const skill of skills) {
    const encoded = Buffer.from(skill.content, 'utf-8').toString('base64');
    psLines.push(`$sd = Join-Path $skillsDir "${skill.name}"`);
    psLines.push('if (-not (Test-Path $sd)) { New-Item -ItemType Directory -Path $sd -Force | Out-Null }');
    psLines.push('$skillPath = Join-Path $sd "SKILL.md"');
    psLines.push('if (Test-Path $skillPath) {');
    psLines.push(`  Write-Host "  SKIP skill (already exists): ${skill.name}"`);
    psLines.push('  $skillSkipped++');
    psLines.push('} else {');
    psLines.push(`  $bytes = [Convert]::FromBase64String("${encoded}")`);
    psLines.push('  [System.IO.File]::WriteAllBytes($skillPath, $bytes)');
    psLines.push(`  Write-Host "  OK skill: ${skill.name}"`);
    psLines.push('  $skillAdded++');
    psLines.push('}');
    psLines.push('');
  }

  // --- MCP Servers ---
  if (Object.keys(mcpServers).length > 0) {
    const mcpJsonEscaped = JSON.stringify(mcpServers).replace(/'/g, "''");
    psLines.push('# Merge MCP configs');
    psLines.push(`$mergeJson = '${mcpJsonEscaped}'`);
    psLines.push('$configPath = Join-Path $env:USERPROFILE ".claude.json"');
    psLines.push('if (Test-Path $configPath) {');
    psLines.push('  $config = Get-Content $configPath -Raw | ConvertFrom-Json');
    psLines.push('} else {');
    psLines.push('  $config = [PSCustomObject]@{}');
    psLines.push('}');
    psLines.push('if (-not ($config.PSObject.Properties.Name -contains "mcpServers")) {');
    psLines.push('  $config | Add-Member -NotePropertyName "mcpServers" -NotePropertyValue ([PSCustomObject]@{})');
    psLines.push('}');
    psLines.push('$merge = $mergeJson | ConvertFrom-Json');
    psLines.push('$added = 0; $skipped = 0');
    psLines.push('foreach ($prop in $merge.PSObject.Properties) {');
    psLines.push('  if (-not ($config.mcpServers.PSObject.Properties.Name -contains $prop.Name)) {');
    psLines.push('    $config.mcpServers | Add-Member -NotePropertyName $prop.Name -NotePropertyValue $prop.Value');
    psLines.push('    $added++');
    psLines.push('    Write-Host "  OK mcp: $($prop.Name)"');
    psLines.push('  } else {');
    psLines.push('    $skipped++');
    psLines.push('    Write-Host "  SKIP mcp (already exists): $($prop.Name)"');
    psLines.push('  }');
    psLines.push('}');
    psLines.push('$config | ConvertTo-Json -Depth 10 | Set-Content $configPath -Encoding UTF8');
    psLines.push('Write-Host "  MCP: $added added, $skipped skipped"');
    psLines.push('');
  }

  // --- Summary ---
  psLines.push('Write-Host ""');
  psLines.push('Write-Host "========================================="');
  psLines.push('Write-Host "  Installation Complete!"');
  psLines.push('Write-Host "========================================="');
  psLines.push('Write-Host "  Commands: $commandAdded added, $commandSkipped skipped"');
  psLines.push('Write-Host "  Skills:   $skillAdded added, $skillSkipped skipped"');
  psLines.push(`Write-Host "  MCP:      ${Object.keys(mcpServers).length}"`);
  psLines.push('Write-Host ""');
  psLines.push('Write-Host "Restart Claude CLI to apply changes."');

  const psScript = psLines.join('\r\n');
  const psBase64 = Buffer.from(psScript, 'utf16le').toString('base64');

  // Wrap in a .bat file that invokes the encoded PowerShell
  const batLines = [];
  batLines.push('@echo off');
  batLines.push('REM Claude CLI Skill Pack Installer (Windows)');
  batLines.push('REM Generated by Claude Code UI');
  batLines.push('');
  batLines.push(`powershell -NoProfile -ExecutionPolicy Bypass -EncodedCommand ${psBase64}`);
  batLines.push('');
  batLines.push('pause');

  return batLines.join('\r\n');
}

// ─── Routes ──────────────────────────────────────────────────

/**
 * GET /api/skill-pack/info
 * Returns counts of commands, skills, and MCP servers.
 */
router.get('/info', async (req, res) => {
  try {
    const userId = req.user?.id;
    const [commands, skills, mcpServers] = await Promise.all([
      collectCommandFiles(COMMANDS_DIR),
      collectSkillFiles(SKILLS_DIR),
      collectMcpServers(userId),
    ]);

    const commandCount = commands.length;
    const skillCount = skills.length;
    const mcpCount = Object.keys(mcpServers).length;

    res.json({
      commands: commandCount,
      skills: skillCount,
      mcpServers: mcpCount,
      package: {
        commands: commandCount,
        skills: skillCount,
        mcpServers: mcpCount,
        total: commandCount + skillCount,
      },
      installPolicy: {
        commands: 'add_if_missing',
        skills: 'add_if_missing',
        mcpServers: 'add_if_missing',
      },
      estimate: {
        canPredictLocalResult: false,
        reason: 'client_local_state_unavailable',
      },
    });
  } catch (error) {
    console.error('Skill pack info error:', error);
    res.status(500).json({
      error: 'Failed to collect skill pack info',
      message: error.message,
    });
  }
});

/**
 * GET /api/skill-pack/download?platform=mac|windows
 * Dynamically generates and downloads an installation script.
 */
router.get('/download', async (req, res) => {
  try {
    const { platform } = req.query;
    const userId = req.user?.id;

    if (!platform || !['mac', 'windows'].includes(platform)) {
      return res.status(400).json({
        error: 'Invalid platform',
        message: 'Query parameter "platform" must be "mac" or "windows".',
      });
    }

    const [commands, skills, mcpServers] = await Promise.all([
      collectCommandFiles(COMMANDS_DIR),
      collectSkillFiles(SKILLS_DIR),
      collectMcpServers(userId),
    ]);

    if (platform === 'mac') {
      const script = generateMacScript(commands, skills, mcpServers);
      res.setHeader('Content-Type', 'application/octet-stream');
      res.setHeader('Content-Disposition', 'attachment; filename="claude-skill-pack-install.command"');
      return res.send(script);
    } else {
      const script = generateWindowsScript(commands, skills, mcpServers);
      res.setHeader('Content-Type', 'application/octet-stream');
      res.setHeader('Content-Disposition', 'attachment; filename="claude-skill-pack-install.bat"');
      return res.send(script);
    }
  } catch (error) {
    console.error('Skill pack download error:', error);
    res.status(500).json({
      error: 'Failed to generate skill pack',
      message: error.message,
    });
  }
});

export default router;
